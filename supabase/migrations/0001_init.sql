drop table if exists csat cascade;
drop table if exists sales cascade;
drop table if exists clients cascade;
drop table if exists coaches cascade;

create table coaches (
  id          bigint generated by default as identity primary key,
  name        text not null,
  role        text,            -- Advance | Basic | Dietician
  team        text,            -- Advance | Basic
  email       text,
  status      text default 'active',
  joined_date date
);

create table clients (
  id             bigint generated by default as identity primary key,
  name           text not null,
  coach_id       bigint references coaches(id),
  plan           text,
  status         text,         -- active | paused | cnr
  start_date     date,
  end_date       date,
  weight_lost_kg numeric
);

create table sales (
  id         bigint generated by default as identity primary key,
  sale_date  date not null,
  client_id  bigint references clients(id),
  coach_id   bigint references coaches(id),
  plan_name  text,
  amount     numeric not null,
  sale_type  text            -- new | renew | reactivate | extension | referral | product
);

create table csat (
  id          bigint generated by default as identity primary key,
  rating_date date not null,
  client_id   bigint references clients(id),
  coach_id    bigint references coaches(id),
  rating      numeric,        -- 1..5
  category    text            -- diet | exercise
);

